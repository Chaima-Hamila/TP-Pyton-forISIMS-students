liste = [17, 38, 10, 25, 72]
#Trier la liste
liste.sort()
#Affichage de la liste
print(liste)
#Ajouter l'element 12 et afficher
liste.append(12)
print(liste)
#Renvercer et afficher
liste.reverse()
print(liste)
#Affiche l'indice de l'element 17
print("L'indice de l'element 17 est : ", liste.index(17))
#Affichage de la sous-liste du 2e au 3e element
print(liste[1:3])
#Affichage de la sous-liste du debut au 2e element
print(liste[:3])
#Affichage de la sous-liste du 3e element a la fin de la liste
print(liste[2:])
#Affichage de la sous-liste complete de la liste
print(liste[:])
#Affichage de la dernier element
print("Le dernier element : ", liste[-1])





